package ch02;

public class Test03 {
  public static void main(String[] args) {
     // 12,3456,7890 정수 값을 자릿수를 포함하여 표현 하시오 
     //3자리 마다 자릿수 표기할것 
       System.out.println(1234_567_890);        
     // 4자리 마다 자릿수 포기할것 
       System.out.println(12_3456_7890); 
       //0b01100001 2진수 자릿수를 포함하여 표현하라 
       //4비트마다 자릿수 표기할것 
       System.out.println(0b0110_0001); 
         }
}

