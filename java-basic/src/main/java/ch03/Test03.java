// 계산결과는 그 변수의 타입과 같다.
package ch03;

public class Test03 {

  public static void main(String[] args) {

    int a = 5;
    int b = 2;
    int c = 5 / 2;
    System.out.println(a / b);  //int 와 int 의 계산 결과는  int 이다.

    double d = 7.3;
    double e = 2.5;
    System.out.println(d);
    System.out.println(e);
    System.out.println(d + e);
    // 위의 예제로 계산결과는 그 변수의 타입과 같다는게 증명!

    double d1 = 9876.543;
    double d2 = 12.34567;
    System.out.println(d1);
    System.out.println(d2);
    System.out.println(d1 + d2);  // double 과 double 의 계산 결과는 double

  }

}




